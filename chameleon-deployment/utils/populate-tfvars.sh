#!/bin/sh

usage=$(cat <<EOF
populate-tfvars.sh <lease-name> [-g] (optional)

-g
    Use gpu. Defaults to cpu only.
EOF
     )

lease_name=$1

if [ -z "${lease_name}" ]; then
    echo "You must provide a lease name"
    echo "${usage}"
    exit 1
fi

while getopts g: optarg; do
    case "${optarg}" in
        g) use_gpu=true
           ;;
        *) echo "${usage}"; exit 1
    esac
done

RESERVATION_ID_CPU=$(openstack reservation lease show "${lease_name}-cpu" -f value -c reservations | \
                         jaq -c 'select(.resource_type == "physical:host") | .id')

if [ "${use_gpu}" = true ]; then
    RESERVATION_ID_GPU=$(openstack reservation lease show "${lease_name}-gpu" -f value -c reservations | \
                             jaq -c 'select(.resource_type == "physical:host") | .id')
else
    RESERVATION_ID_GPU="\"\""
fi

cat << EOF > ../terraform/terraform.tfvars
# This file was autogenerated by populate-tfvars.sh
reservation_id_cpu = ${RESERVATION_ID_CPU}
reservation_id_gpu = ${RESERVATION_ID_GPU}
EOF
